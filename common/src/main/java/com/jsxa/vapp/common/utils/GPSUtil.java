        System.out.println("1--->百度直接转wgs-84:" + convert6Digit(BD[1]) + "," + convert6Digit(BD[0]));
        System.out.println("2--->百度转GCJ2后再转wgs-84:" + BDGCJ284[1] + "," + BDGCJ284[0]);
